// Arithmetic Operators = Operands (values, Variables, etc..)
// Operator (+,-,*,/,%);
// ex. 11 = x + 5;

let student = 30;

console.log(`Total Students: ${student}`);
console.log(`Total Students: ${student + 5}`); // Addition 
console.log(`Total Students: ${student - 5}`); // subtraction
console.log(`Total Students: ${student * 5}`);  // multiplication
console.log(`Total Students: ${student / 5}`); // division
console.log(`Total Students: ${student % 5}`);  // modulus

student +=54; // student = student + 54;
student -=54;
student *=54;
student /=54;
student %=54;
console.log(`Total Students: ${student}`); // 84

// exponentition
console.log(`Total Students: ${student ** 2}`);  // 84 * 84 = 7056

// Increment and Decrement
let x =5;
console.log(`x: ${x}`);
x++; // x = x + 1;
x--; // x = x - 1;
console.log(`x: ${x}`); // 6

